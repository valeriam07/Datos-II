
public class calculadora {

    void sumar(int x, int y) {
        int res = x + y;
        System.out.println("El resultado de la suma es: " + res);
    }

    void restar(int x, int y) {
        int res = x - y;
        System.out.println("El resultado de la resta es: " + res);
    }

    void multiplicar(int x, int y) {
        int res = x * y;
        System.out.println("El resultado de la multiplicacion es: " + res);
    }

    void dividir(int x, int y) {
        int res = x / y;
        System.out.println("El resultado de la division es: " + res);
    }


}

